package com.trodev.bangladorjiadmin.men;

public class Ordertshirt {

    public String size, length, width, color,fabt, tgsm, quan, home, phn, date, time;

    public Ordertshirt() {
    }

    public Ordertshirt(String size, String length, String width, String color, String fabt, String tgsm, String quan, String home, String phn, String date, String time) {
        this.size = size;
        this.length = length;
        this.width = width;
        this.color = color;
        this.fabt = fabt;
        this.tgsm = tgsm;
        this.quan = quan;
        this.home = home;
        this.phn = phn;
        this.date = date;
        this.time = time;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFabt() {
        return fabt;
    }

    public void setFabt(String fabt) {
        this.fabt = fabt;
    }

    public String getTgsm() {
        return tgsm;
    }

    public void setTgsm(String tgsm) {
        this.tgsm = tgsm;
    }

    public String getQuan() {
        return quan;
    }

    public void setQuan(String quan) {
        this.quan = quan;
    }

    public String getHome() {
        return home;
    }

    public void setHome(String home) {
        this.home = home;
    }

    public String getPhn() {
        return phn;
    }

    public void setPhn(String phn) {
        this.phn = phn;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
